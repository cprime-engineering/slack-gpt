"""Module bare bones prompt templates."""


class RFPCopilotTemplate:
    """Default template."""

    def __init__(self):
        """Constructor."""

    def greeting(self):
        """Return a greeting for display by the console application."""
        return "Hi, I'm Ada, your RFP Support Specialist. How can I help you today?"

    def help(self):
        """Describe how this agent can provide RFP support."""
        return "I can help with writing RFP responses, finding answers to questions, and more."
